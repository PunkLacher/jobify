import Job from "../models/JobModel.js";
import { StatusCodes } from "http-status-codes";
import mongoose from "mongoose";
import day from "dayjs";

//---------------------------------
//Get All Jobs
export const getAllJobs = async (req, res) => {
  //destrutcure query string
  const { search, jobStatus, jobType, sort } = req.query;
  //create object from query string for use in Mongo find method
  const queryObject = {
    createdBy: req.user.userId,
  };
  //if query string is not empty, add to query object .USe Regex to make it a non exact search
  if (search) {
    //the below is Mongo syntax for searching either or based on query stirng
    queryObject.$or = [
      { position: { $regex: search, $options: "i" } },
      { company: { $regex: search, $options: "i" } },
    ];
  }
  //if jobStatus comes through as query string and isn;t set to all, add to query object
  if (jobStatus && jobStatus !== "all") {
    queryObject.jobStatus = jobStatus;
  }
  //if jobType comes through as query string and isn;t set to all, add to query object
  if (jobType && jobType !== "all") {
    queryObject.jobType = jobType;
  }

  const sortOptions = {
    newest: "-createdAt",
    oldest: "createdAt",
    "a-z": "position",
    "z-a": "-position",
  };

  const sortKey = sortOptions[sort] || sortOptions.newest;

  // setup pagination
  const page = Number(req.query.page) || 1;
  const limit = Number(req.query.limit) || 10;
  const skip = (page - 1) * limit;

  // MongoDb find method to query DB for jobs based on Query Object, send back based on pagination settings from front end
  const jobs = await Job.find(queryObject)
    .sort(sortKey)
    .skip(skip)
    .limit(limit);

  const totalJobs = await Job.countDocuments(queryObject);
  const numOfPages = Math.ceil(totalJobs / limit)
  res.status(StatusCodes.OK).json({ totalJobs, numOfPages, currentPage:page,jobs });
};

